    downloadBinary: helpers.downloadBinary,
    getCompiledMemoryMap: runtime.getCompiledMemoryMap,
    getCompiledSymbols: runtime.getCompiledSymbols,
    getCompiledListing: runtime.getCompiledListing,
    copyText: helpers.copyText,
