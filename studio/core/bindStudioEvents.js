    updateOptimizationHint: helpers.updateOptimizationHint,
    scheduleEditorInsightsRefresh: helpers.scheduleEditorInsightsRefresh,
    syncUiFromProject: helpers.syncUiFromProject,
    setStatus: helpers.setStatus,
